    private $html_menus = "";
    private $index = 1;
    private function get_html_menus($data = null,$root = 0,$level = '', $table = true , $activer = -1){
        $termsList = array();
        $new_listdata = array();
        if ($root != 0)
        {
            $level .= '&mdash; &mdash;';
        }
        if ($data != null) { 
            foreach ($data AS $key => $item )
            {
                if ($item->pid == $root)
                {
                    $termsList[] = ($item);
                }
                else
                {
                    $new_listdata[] = ($item);
                }
            }
        }
        if ($termsList != null)
        {
            foreach ($termsList AS $key => $item_2 )
            {
                $active = '';
                if ($activer == $item_2->id)
                {
                    $active = 'selected';
                }
                if ($table == false)
                {
                    $this->html_menus .= '<option value="' . $item_2->id . '" '. $active . '>' . $level . '  ' . $item_2->name . '</option>';
                    
                }
                else
                {
                    $this->html_menus .= '<tr>';
                    $this->html_menus .= '<td>'.($this->index++).'</td>';
                    $this->html_menus .= '<td>' . $level . '  ' . $item_2->name . '</td>';
                    $this->html_menus .= '<td>' . $item_2->path . '</td>';
                    $this->html_menus .= '<td><input type="number" name = "order[]" value ="'. $item_2->sort .'"><input type="hidden" name = "idModel[]" value ="'. $item_2->id . '"> </td>';
                    $this->html_menus .='<td>'.$item_2->status.'</td>';
                    $this->html_menus .= '<td><a id="edit-action" href = "'.route($this->_ROUTE_FIX.".".$this->_ROUTE.".edit",["id" => $item_2->id]).'" style = "margin-right:5px;">Edit </a> | <a return confirm(\'Do you want to delete this item?\'); id="delete-action" href = "'.route($this->_ROUTE_FIX.".".$this->_ROUTE.".destroy",["id" => $item_2->id]).'"> Delete</a> </td>';
                    $this->html_menus .= '</tr>';
                }
                $this->get_html_menus($new_listdata, $item_2->id, $level, $table, $activer);
            }
        }
        return $this->html_menus;
    } 
    public function _TREEMENUS($trees = [] ,$root = 0){
        if($root == 0) 
            echo  '<ol class="sortable">';
        else 
            echo '<ol class="menu-group">';
        foreach ($trees as $key => $value) {
            if($value->pid === $root){
                unset($trees[$key]);
                echo '<li class="item-menu" id="list_'.$value->id.'">
                    <div class="content-box">'.$value->name.'</div>';
                    $this->_TREEMENUS($trees,$value->id);
                    echo '<div class="ns-actions">
